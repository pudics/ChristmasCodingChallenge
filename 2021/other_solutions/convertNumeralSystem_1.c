/**
 * The inputBinaryNumber shall be interpreted as a binary number (so the largest valid input is 1111111111).
 * The function shall return with the base-targetNumeralSystem converted value of the inputBinaryNumber.
 * The input targetNumeralSystem may be assumed to be in range [3, 10].
 * If inputBinaryNumber contains other digits than 0 and 1, then return value shall be 0xFFFFFFFF.
 *
 * @param  uint32 inputBinaryNumber: the binary input number to be converted
 * @param  uint8 targetNumeralSystem: inputBinaryNumber shall be converted to this numeral system
 * @return uint32: the base-targetNumeralSystem converted value of inputBinaryNumber
 */
uint32 convertNumeralSystem(uint32 inputBinaryNumber, uint8 targetNumeralSystem)
{
    /* Normal representation of the input value*/
    uint32 normalNumber = 0u;
    /* Counter for digits */
    uint32 counter = 0u;
    /* Actual digit under processing */
    uint32 digit = 0u;
    /* converted return value*/
    uint32 convertedNumber = 0u;

    /* First step: convert into normal number representation */
    do
    {
        /* Calculate modulo 10 to get the current digit */
        /* the expected digit is either 0 or 1 or other number in case of wrong input */
        digit = inputBinaryNumber % 10u;

        /* Check if digit 1 in the input side we set the corresponding bit on the normal number*/
        if(1u == digit)
        {
            normalNumber |= 1u << counter; /* parasoft-suppress MISRA2004-12_8 "The counter value is maximum 10 therefore this operation is safe and does not violate the uint32 limits." */
        }
        else if (0u != digit) /* if other than zero we found an invalid input */
        {
            convertedNumber = WRONG_INPUT_RESPONSE; /* wrong input arrived */
            break;
        }
        else
        {
            /* In case of 0 no additional action needed. */
        }
        counter++;

        /* divide the input value */
        inputBinaryNumber = inputBinaryNumber / 10u;
    }
    while (0u != inputBinaryNumber);  /* Repeat until the number is processed */

    /* Second step: convert into target numeral system representation */
    if (WRONG_INPUT_RESPONSE != convertedNumber)
    {
        /* In case of the target numeral system is 10 no conversion is needed */
        if (10u == targetNumeralSystem)
        {
            convertedNumber = normalNumber;
        }
        else
        {
            /* Get the number from the lookup table. the numbers are 0..1023. The numeral system is 3..9 */
            convertedNumber = allnumbers[normalNumber][targetNumeralSystem-3u];
        }
    }

    return convertedNumber;
}
